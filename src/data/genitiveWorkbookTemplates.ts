import type { DifficultyId, QuestionTemplate, WordCategory } from '../types';

/** Fill-in-the-blank genitive drills aligned with printed “У / нет / numbers” worksheets. */
const MC_MODES = ['practice', 'speed_round', 'boss_battle'] as const;

function wbGen(
  id: string,
  prompt: string,
  correct: string,
  distractors: [string, string, string],
  explanation: string,
  options: {
    targetLemmaId?: string;
    targetCategory?: WordCategory;
    helperWord?: string;
    difficulty?: DifficultyId;
    tags: string[];
  },
): QuestionTemplate {
  const {
    targetLemmaId,
    targetCategory = 'noun',
    helperWord = 'у',
    difficulty = 'beginner',
    tags,
  } = options;
  return {
    id,
    type: 'multiple_choice',
    modeIds: [...MC_MODES],
    prompt,
    targetCaseId: 'genitive',
    targetLemmaId,
    targetCategory,
    helperWord,
    questionPrompt: 'кого? чего?',
    correctAnswer: correct,
    acceptedAnswers: [correct],
    distractors: [...distractors],
    explanation,
    difficulty,
    tags: ['genitive', 'workbook', ...tags],
  };
}

